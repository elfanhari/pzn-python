# belajar string method, untuk ubah ke uppercase, cek jumlah huruf, dll.
# source: https://docs.python.org/3.7/library/stdtypes.html#string-methods
name = 'elfan saputra'

print(name) # elfan saputra
print(name.upper()) # ELFAN SAPUTRA
print(name.capitalize()) # Elfan Saputra
print(name.title()) # Elfan saputra
print(name.split()) # ['elfan', 'saputra']
